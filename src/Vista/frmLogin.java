    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        //Validar que ningun campo este vacio
        if (!CValidaciones.validarCampos(pnlLogin)) {
            return;
        }

        CmUsuario usuario = new CmUsuario();
        CmHojaPruebas hojaPruebas = new CmHojaPruebas();
        CmPruebas pruebas = new CmPruebas();
        CmVehiculos vehiculos = new CmVehiculos();

        //Traer Todos los Datos del vehiculo       
        if (vehiculos.idPorPlaca(txtPlaca.getText())) {
            if (!vehiculos.cargarVehiculo(vehiculos.getIdVehiculo())) {
                return;
            }
        } else {
            return;
        }

        //Validar si es una moto y traer los tiempos del motor con el modelo del vehiculo
        if (vehiculos.getIdTipoVehiculo() == 4) {
            tiemposMotor = vehiculos.getTiemposMotor();
            modeloVehiculo = vehiculos.getModelo();
        } else {
            CMensajes.mensajeError("La Placa Ingresada no pertenece a una moto");
            return;
        }
        //Buscar la ultima hoja de pruebas que tenga registrada el numero de placa
        if (hojaPruebas.ultimaHojaPruebasPorVehiculo(vehiculos.getIdVehiculo())) {
            idHojaPruebas = hojaPruebas.getIdHojaPruebas();
        } else {
            return;
        }
        //Validar si la hoja de pruebas tiene la prueba a la cual se quiere acceder        

        //Validar si el usuario y contrase単a son correctos
        if (usuario.validarSesion(txtUsuario.getText(), new String(txtContrase単a.getPassword()))) {
            idUsuario = usuario.getIdUsuario();
            frmMenuPrincipal principal = new frmMenuPrincipal();
            principal.setExtendedState(JFrame.MAXIMIZED_BOTH);
            principal.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

